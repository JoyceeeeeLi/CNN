(x_train, y_train), (x_test, y_test) = keras.datasets.mnist.load_data()
print(x_train.shape)
x_train = x_train.astype("float32") / 255
x_test = x_test.astype("float32") / 255

x_train = np.expand_dims(x_train, axis=-1)
x_train = tf.image.resize(x_train, [32,32])

x_test = np.expand_dims(x_test, axis=-1)
x_test = tf.image.resize(x_test, [32,32])

print(x_train.shape)
print(x_test.shape)

y_train = keras.utils.to_categorical(y_train, 10)
y_test = keras.utils.to_categorical(y_test, 10)

input_shape = (32, 32, 1)

### a
model = keras.Sequential(
    [
        keras.Input(shape=input_shape),
        layers.Conv2D(64, kernel_size=(3, 3), activation="relu", padding='same'),
        layers.MaxPooling2D(pool_size=(2, 2)),
        layers.Conv2D(128, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.MaxPooling2D(pool_size=(2, 2)),
        layers.Conv2D(256, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.Conv2D(256, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.MaxPooling2D(pool_size=(2, 2)),
        layers.Conv2D(512, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.Conv2D(512, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.MaxPooling2D(pool_size=(2, 2)),
        layers.Conv2D(512, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.Conv2D(512, kernel_size=(3, 3), activation="relu",  padding='same'),
        layers.MaxPooling2D(pool_size=(2, 2)),
        layers.Flatten(),
        layers.Dense(4096, activation="relu"),
        layers.Dropout(0.5),
        layers.Dense(4096, activation="relu"),
        layers.Dropout(0.5),
        layers.Dense(10, activation="softmax"),
    ]
)
model.summary()

batch_size = 256
epochs = 5

model.compile(loss=keras.losses.categorical_crossentropy, optimizer=keras.optimizers.Adam(lr=0.00001), metrics=['accuracy'])
history = model.fit(x_train, y_train, batch_size=batch_size, epochs=5, verbose=1, validation_data=(x_test, y_test), steps_per_epoch=60000/batch_size)
score = model.evaluate(x_test, y_test, verbose=0)
print("Test loss:", score[0])
print("Test accuracy:", score[1])

advConfig = nsl.configs.make_adv_reg_config(adv_step_size=0.2,adv_grad_norm='infinity')
advModel = nsl.keras.AdversarialRegularization(
  model,
  label_keys=['label'],
  adv_config=advConfig
)

advModel.compile(loss=keras.losses.categorical_crossentropy, optimizer=keras.optimizers.Adam(lr=0.00001), metrics=['accuracy'])
history = advModel.fit({'feature': x_train, 'label': y_train}, batch_size=batch_size, epochs=5, verbose=1, steps_per_epoch=60000/batch_size)

score = advModel.evaluate({'feature': x_test, 'label': y_test}, verbose=0)
print("Test loss:", score[0])
print("Test accuracy:", score[2])

def FGSM(model, X, y, eps):
  X = tf.cast(X, tf.float32)
  with tf.GradientTape() as tape:
    tape.watch(X)
    pred = model(X)
    loss = keras.losses.categorical_crossentropy(y, pred)
  gradient = tape.gradient(loss, X)
  signedGradient = tf.sign(gradient)
  adv = (X + (signedGradient*eps)).numpy()
  return adv

adv = FGSM(model, x_test, y_test, 0.2)

